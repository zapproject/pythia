uint32_t hc_rotl32_S(uint32_t x, uint32_t s) {
  return (x << s) | (x >> (32 - s));
}

uint32_t hc_rotl32(uint32_t x, uint32_t s) {
  return (x << s) | (x >> (32 - s));
}

